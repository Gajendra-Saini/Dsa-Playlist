#include <iostream>
using namespace std;

int main()
{
    //programme for airmatic operator
    /* 
1.plus(+)
2.minus(-)
3.divide(/)
4.multiply(*)
5.reminder(%) also called mod
*/
    int a, b;
    cin >> a >> b;
    cout << "Sum is :" << a + b << endl;
    cout << "Divide is :" << a / b << endl;
    cout << "Minus is :" << a - b << endl;
    cout << "Multiply is :" << a * b << endl;
    cout << "Reminder is :" << a % b << endl;
    cout << "Happy Learning :)";
    return 0;
}